const fn four_cc_to_u32(four_cc: FourCC) -> u32 {
    (four_cc.0[0] as u32)
        + ((four_cc.0[1] as u32) << 8)
        + ((four_cc.0[2] as u32) << 16)
        + ((four_cc.0[3] as u32) << 24)
}

